/*
 * function:    Func_Port_40_Pin_0_init
 * returns:     HAL_Return_Type error report
 * parameters:  void
 * description: The function initializes Port 40 Pin 0 (P40.0) of TC375 for operation of the controller
 */
HAL_Return_Type Func_Port_40_Pin_0_init (void)
{
    HAL_Return_Type tmp;

    /*
     * set P40.0 to input
     */
    P40_IOCR0.B.PC0 = 0x0;

    if (P40_IOCR0.B.PC0 != 0x0)
    {
        tmp = HAL_E_NOT_OK;
    }
    else
    {
        tmp = HAL_E_OK;
    }

    return tmp;
}


